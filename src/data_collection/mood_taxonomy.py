def get_mood_for_tag(tag: str) -> str | None:
    tag = tag.lower().strip()
    for mood, synonym in MOOD_TAXONOMY.items():
        if tag in synonym or tag == mood:
            return mood
    return None

def get_mood_for_palette(tags: list[str]) -> str | None:
    for tag in tags:
        mood = get_mood_for_tag(tag)
        if mood:
            return mood
    return None

